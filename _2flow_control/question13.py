""" This program will demonstrate Question 13 from Chapter 2 """

def from1to10():
    """This function will print 1 to 10 using a for loop"""
    for i in range(1, 11):
        print(i)
    print()

from1to10()

def from1to10while():
    """This function will print 1 to 10 using a while loop"""
    i = 1
    while i <= 10:
        print(i)
        i += 1

from1to10while()
